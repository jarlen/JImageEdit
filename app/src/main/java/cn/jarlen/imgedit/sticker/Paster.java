package cn.jarlen.imgedit.sticker;

public class Paster {
    private String pasterPath;
    private String pasterName;

    public String getPasterPath() {
        return pasterPath;
    }

    public void setPasterPath(String pasterPath) {
        this.pasterPath = pasterPath;
    }

    public String getPasterName() {
        return pasterName;
    }

    public void setPasterName(String pasterName) {
        this.pasterName = pasterName;
    }
}
